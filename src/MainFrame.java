    private void scheduleCoursesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesActionPerformed
        // TODO add your handling code here:
        String courseCode = scheduleCourseCourseComboBox.getSelectedItem().toString();
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        CourseEntry course = null;
        for (CourseEntry x: courses){
            if (x.getCourseCode().equals(courseCode)){
                course = x;
            }
        }
        StudentEntry student = getStudentFromComboBox(scheduleCourseStudentComboBox.getSelectedIndex());

        Long datetime = System.currentTimeMillis();
        Timestamp timestamp = new Timestamp(datetime);
        ScheduleEntry schedEnter;
        int seatsTaken = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        if(seatsTaken < course.getSeats()){
            schedEnter = new ScheduleEntry(currentSemester, student.getStudentID(), courseCode, "S", timestamp);
            scheduleStudentStatusLabel.setText(student.getLastName() + ", " + student.getFirstName() + " has been Scheduled for " + courseCode);
        } else {
            schedEnter = new ScheduleEntry(currentSemester, student.getStudentID(), courseCode, "W", timestamp);
            scheduleStudentStatusLabel.setText(student.getLastName() + ", " + student.getFirstName() + " has been Waitlisted for " + courseCode);
        }
        ScheduleQueries.addScheduleEntry(schedEnter);
    }//GEN-LAST:event_scheduleCoursesActionPerformed

