    private void DisplaySelectedCourseAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplaySelectedCourseAdminButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<ScheduleEntry> scheduledStudentsSchedules =  ScheduleQueries.getScheduledStudentsByCourse(currentSemester, displayCourseListOfStudentsComboBox.getSelectedItem().toString());
        ArrayList<ScheduleEntry> waitlistedStudentsSchedules =  ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, displayCourseListOfStudentsComboBox.getSelectedItem().toString());
        ArrayList<StudentEntry> scheduledStudents = new ArrayList<StudentEntry>();
        ArrayList<StudentEntry> waitlistedStudents = new ArrayList<StudentEntry>();

        for (ScheduleEntry schedule: scheduledStudentsSchedules){
            scheduledStudents.add(StudentQueries.getStudent(schedule.getStudentID()));
        }
        
        for (ScheduleEntry schedule: waitlistedStudentsSchedules){
            waitlistedStudents.add(StudentQueries.getStudent(schedule.getStudentID()));
        }
        DefaultTableModel displayScheduledStudentsTable = (DefaultTableModel) scheduledStudentsTable.getModel();
        displayScheduledStudentsTable.setNumRows(0);
        for (StudentEntry student: scheduledStudents){
            displayScheduledStudentsTable.addRow(new Object[]{student.getLastName(), student.getFirstName(), student.getStudentID()});
        }       
        
        DefaultTableModel displayWaitlistedStudentsTable = (DefaultTableModel) waitlistedStudentsTable.getModel();
        displayWaitlistedStudentsTable.setNumRows(0);
        for (StudentEntry student: waitlistedStudents){
            displayWaitlistedStudentsTable.addRow(new Object[]{student.getLastName(), student.getFirstName(), student.getStudentID()});
        }
    }//GEN-LAST:event_DisplaySelectedCourseAdminButtonActionPerformed

    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = getStudentFromComboBox(dropStudentComboBox.getSelectedIndex());
        dropStudentTextArea.setText(dropStudentComboBox.getSelectedItem().toString() + " has been dropped from the list of students.");
        StudentQueries.dropStudent(student.getStudentID());
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester: semesters){
            dropStudentTextArea.append("\n\nFor Semester: " + semester);
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, student.getStudentID());
            for (ScheduleEntry schedule: schedules){
                if(schedule.getStatus().equals("S")){
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentTextArea.append("\n" + dropStudentComboBox.getSelectedItem().toString() + " " + student.getStudentID() + " "  + "has been dropped from " + schedule.getCourseCode());
                    ScheduleEntry waitlistOff = checkWaitlist(semester, schedule.getCourseCode());
                    if (waitlistOff != null){
                        StudentEntry studentOff = StudentQueries.getStudent(waitlistOff.getStudentID());
                        dropStudentTextArea.append("\n" + studentOff.getLastName() + ", " + studentOff.getFirstName() + " " + waitlistOff.getStudentID() + " "  +   " has been scheduled into " + schedule.getCourseCode());
                        ScheduleQueries.updateScheduleEntry(semester, waitlistOff);
                    }
                } else {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, student.getStudentID(), schedule.getCourseCode());
                    dropStudentTextArea.append("\n" + dropStudentComboBox.getSelectedItem().toString() + " " + student.getStudentID() + " " + " has been dropped from the waitlist for " + schedule.getCourseCode());
                }
            }
        }
        rebuildStudentComboBox();
    }//GEN-LAST:event_dropStudentButtonActionPerformed

    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = dropCourseComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> waitlistedStudentsSchedules =  ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, courseCode);
        ArrayList<ScheduleEntry> scheduledStudentsSchedules =  ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        dropCourseTextArea.setText("Scheduled Students dropped from the course: ");
        for(ScheduleEntry schedule: scheduledStudentsSchedules){
            StudentEntry student = StudentQueries.getStudent(schedule.getStudentID());
            dropCourseTextArea.append("\n"+ student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID());
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, schedule.getStudentID(), courseCode);
        }
        dropCourseTextArea.append("\n\nWaitlisted Students dropped from the course: ");
        for(ScheduleEntry schedule: waitlistedStudentsSchedules){
            StudentEntry student = StudentQueries.getStudent(schedule.getStudentID());
            dropCourseTextArea.append("\n"+ student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID());
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, schedule.getStudentID(), courseCode);
        }
        CourseQueries.dropCourse(currentSemester, courseCode);
    }//GEN-LAST:event_dropCourseButtonActionPerformed

    private void studentDropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentDropCourseButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = dropCourseFromStudentComboBox.getSelectedItem().toString();
        StudentEntry student = getStudentFromComboBox(dropCourseFromStudentStudentComboBox.getSelectedIndex());
        studentDropCourseTextArea.setText(dropCourseFromStudentStudentComboBox.getSelectedItem().toString() + " " + student.getStudentID() + " has been dropped from " + courseCode);
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), courseCode);
        ScheduleEntry waitlistOff = checkWaitlist(currentSemester, courseCode);
        
        if (!(waitlistOff == null)) {
            StudentEntry studentOff = StudentQueries.getStudent(waitlistOff.getStudentID());
            studentDropCourseTextArea.append("\n" + studentOff.getLastName() + ", " + studentOff.getFirstName() +  " has been scheduled into " + courseCode);
            ScheduleQueries.updateScheduleEntry(currentSemester, waitlistOff);
        }
    }//GEN-LAST:event_studentDropCourseButtonActionPerformed

    private ScheduleEntry checkWaitlist(String semester, String courseCode){
        ScheduleEntry schedule = null;
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getWaitlistedStudentsByCourse(semester, courseCode);
        if(!schedules.isEmpty()){
            schedule = schedules.get(0);
        }
        return schedule;
    }
    
    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            project = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            project = "Course Scheduler Spring 2023";

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(project.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

